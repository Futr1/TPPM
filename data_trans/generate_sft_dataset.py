def build_grouped_payload(samples: list[DistillSample]) -> list[dict[str, Any]]:
    """Group samples by session_id, preserving insertion order."""
    groups: dict[str, dict[str, Any]] = {}
    for sample in samples:
        sid = sample.session_id
        if sid not in groups:
            groups[sid] = {
                "session_id": sid,
                "source_index": sample.source_index,
                "samples": [],
            }
        groups[sid]["samples"].append(
            {
                "user_turn_index": sample.user_turn_index,
                "instruction": sample.instruction,
                "output": sample.output,
            }
        )
    return list(groups.values())


def main() -> int:
    args = parse_args()
    samples = asyncio.run(run_generation(args))
    payload = build_grouped_payload(samples)
    write_json(args.output, payload)
    print(f"[DONE] wrote SFT data: {args.output}")
    print(f"[DONE] sessions={len(payload)}")
    print(f"[DONE] samples={sum(len(g["samples"]) for g in payload)}")
    if args.failed_output.exists():
        print(f"[DONE] failure log: {args.failed_output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
