def utc_now_iso() -> str:
    return datetime.now(timezone.utc).isoformat()


def load_json(path: Path) -> Any:
    with path.open("r", encoding="utf-8") as file:
        return json.load(file)


def write_json(path: Path, payload: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as file:
        json.dump(payload, file, ensure_ascii=False, indent=2)


def append_failed(path: Path, record: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    record = dict(record)
    record["timestamp"] = utc_now_iso()
    with path.open("a", encoding="utf-8") as file:
        file.write(json.dumps(record, ensure_ascii=False) + "\n")


def normalize_dataset(payload: Any) -> list[dict[str, Any]]:
    if isinstance(payload, list):
        return [item for item in payload if isinstance(item, dict)]

    if isinstance(payload, dict):
        if isinstance(payload.get("messages"), list):
            return [payload]
        for key in ("data", "conversations", "sessions", "items"):
            value = payload.get(key)
            if isinstance(value, list):
                return [item for item in value if isinstance(item, dict)]

    raise ValueError("Unsupported PsyDial-D4 structure.")


def default_session_id(index: int) -> str:
    return f"psydial_d4_{index:05d}"


def session_id_for(index: int, item: dict[str, Any]) -> str:
    for key in ("session_id", "id", "dialogue_id", "conversation_id"):
        value = item.get(key)
        if value is not None and str(value).strip():
            return str(value).strip()
    return default_session_id(index)


def source_index_from_session_id(session_id: str) -> int | None:
    prefix = "psydial_d4_"
    if not session_id.startswith(prefix):
        return None
    try:
        return int(session_id[len(prefix) :])
    except ValueError:
        return None


def normalize_messages(item: dict[str, Any]) -> list[dict[str, str]]:
    messages = item.get("messages")
    if not isinstance(messages, list):
        return []

    normalized: list[dict[str, str]] = []
    for message in messages:
        if not isinstance(message, dict):
            continue
        role = str(message.get("role", "")).strip().lower()
        if role not in {"system", "user", "assistant"}:
            continue

        content = message.get("content", "")
        if isinstance(content, list):
            content = "\n".join(
                str(block.get("text", block)) if isinstance(block, dict) else str(block)
                for block in content
            )
        content_text = str(content).strip()
        if content_text:
            normalized.append({"role": role, "content": content_text})
    return normalized


def compact_user_text(text: str) -> str:
    return " ".join(str(text).strip().split())


def is_low_value_user_text(text: str) -> bool:
    compacted = compact_user_text(text)
    if not compacted:
        return True

    normalized = compacted.strip("。！？!?,，~～. ")
    if normalized in LOW_VALUE_USER_TEXTS:
        return True

    if len(normalized) <= 3 and all(char in "嗯哦啊额好的是对行吧" for char in normalized):
        return True

    return False


def score_user_input(text: str, *, min_input_chars: int, max_input_chars: int) -> tuple[float, list[str]]:
    """Score a post-memory user turn by length and psychological relevance."""

    compacted = compact_user_text(text)
    char_len = len(compacted)
    score = 0.0
    reasons: list[str] = []

    if is_low_value_user_text(compacted):
        return 0.0, ["low_value_ack"]

    if char_len < min_input_chars:
        return 0.0, ["too_short"]

    if max_input_chars > 0 and char_len > max_input_chars:
        score -= 0.4
        reasons.append("too_long")

    if char_len >= min_input_chars:
        score += 0.4
        reasons.append("length_min")
    if char_len >= 25:
        score += 0.3
        reasons.append("length_25")
    if char_len >= 50:
        score += 0.3
        reasons.append("length_50")
    if char_len >= 100:
        score += 0.2
        reasons.append("length_100")

    matched_groups = 0
    matched_keywords = 0
    for group_name, keywords in PSYCHOLOGICAL_KEYWORD_GROUPS.items():
        group_hits = [keyword for keyword in keywords if keyword in compacted]
        if group_hits:
            matched_groups += 1
            matched_keywords += len(group_hits)
            reasons.append(f"{group_name}:{','.join(group_hits[:3])}")

    if matched_groups:
        score += min(1.2, 0.45 * matched_groups)
        score += min(0.8, 0.12 * matched_keywords)

    if any(marker in compacted for marker in ("我", "自己", "我的", "让我", "给我")):
        score += 0.25
        reasons.append("self_reference")

    if any(marker in compacted for marker in ("？", "?", "怎么办", "为什么", "如何", "怎么")):
        score += 0.25
        reasons.append("question_or_help")

    return max(score, 0.0), reasons


def user_inputs_after_context(
    item: dict[str, Any],
    *,
    context_turns: int,
    start_ratio: float | None,
    max_user_inputs: int,
    min_input_chars: int,
    max_input_chars: int,
    min_input_score: float,
) -> list[tuple[int, str]]:
    """Return filtered user messages after a fixed context boundary or ratio boundary."""

    messages = normalize_messages(item)
    non_system_messages = [message for message in messages if message["role"] != "system"]
    if start_ratio is not None:
        if not 0.0 <= start_ratio < 1.0:
            raise ValueError("--start-ratio must be in [0, 1).")
        context_boundary = math.ceil(len(non_system_messages) * start_ratio)
    else:
        context_boundary = context_turns

    non_system_seen = 0
    selected: list[tuple[int, str]] = []
    seen_user_texts: set[str] = set()

    for message in messages:
        role = message["role"]
        if role == "system":
            continue

        non_system_seen += 1
        if non_system_seen <= context_boundary:
            continue

        if role == "user":
            user_text = compact_user_text(message["content"])
            if user_text in seen_user_texts:
                continue

            score, _reasons = score_user_input(
                user_text,
                min_input_chars=min_input_chars,
                max_input_chars=max_input_chars,
            )
            if score < min_input_score:
                continue

            seen_user_texts.add(user_text)
            selected.append((non_system_seen, user_text))
            if max_user_inputs > 0 and len(selected) >= max_user_inputs:
                break

    return selected


def build_memory_index(memory_bank: dict[str, Any]) -> dict[str, list[dict[str, Any]]]:
    sessions = memory_bank.get("sessions", [])
    if not isinstance(sessions, list):
        raise ValueError("memory_bank['sessions'] must be a list.")

    indexed: dict[str, list[dict[str, Any]]] = {}
    for session in sessions:
        if not isinstance(session, dict):
            continue
        session_id = str(session.get("session_id", "")).strip()
        memories = session.get("tppm_memory", [])
        if session_id and isinstance(memories, list):
            indexed[session_id] = [item for item in memories if isinstance(item, dict)]
    return indexed


def format_memory_background(memories: list[dict[str, Any]]) -> str:
    """Convert structured memories to a compact teacher-only profile background."""

    if not memories:
        return "暂无可用的长期画像背景。"

    lines = []
    for idx, memory in enumerate(memories, 1):
        attribute = str(memory.get("attribute", "profile")).strip()
        label = ATTRIBUTE_LABELS.get(attribute, attribute)
        value = str(memory.get("value", "")).strip()
        evidence = str(memory.get("evidence", "")).strip()
        phi = memory.get("phi")

        if not value:
            continue

        suffix = ""
        if isinstance(phi, (int, float)):
            suffix = f"；显著性={float(phi):.3f}"
        if evidence:
            suffix += f"；简要依据={evidence[:120]}"
        lines.append(f"{idx}. {label}: {value}{suffix}")

    return "\n".join(lines) if lines else "暂无可用的长期画像背景。"


def build_system_prompt(memory_background: str) -> str:
    return f"""
你是一名拥有长期记忆能力的专业心理咨询师。

你会看到一段只供你内部参考的【画像背景】，它来自用户此前对话中抽象出的心理画像记忆。
请根据这些长期画像，隐式地理解用户的处境、情绪和应对方式，并回复用户的【最新提问】。

【画像背景】
{memory_background}

回复要求：
1. 回复必须自然、温和、专业，像真实心理咨询师一样陪伴和引导。
2. 严禁直接复述、列举或暴露【画像背景】中的记忆条目。
3. 不要说“根据你的画像”“我记得你的长期记忆”“你的画像显示”等表述。
4. 可以润物细无声地利用画像背景，让回复更贴合用户的长期处境。
5. 不做医学诊断，不贴病理标签，不夸大风险。
6. 每次回复聚焦一个核心点，避免一次问太多问题。
7. 输出最终回复文本即可，不要 Markdown，不要解释生成过程。
""".strip()


async def generate_teacher_reply(
    client: AsyncOpenAI,
    task: DistillTask,
    *,
    model: str,
    max_retries: int,
    initial_backoff: float,
    max_backoff: float,
) -> DistillSample:
    system_prompt = build_system_prompt(task.memory_background)
    last_error: Exception | None = None

    for attempt in range(1, max_retries + 1):
        try:
            response = await client.chat.completions.create(
                model=model,
                temperature=0.7,
                messages=[
                    {"role": "system", "content": system_prompt},
                    {"role": "user", "content": task.instruction},
                ],
            )
            output = (response.choices[0].message.content or "").strip()
            if not output:
                raise ValueError("Teacher returned an empty response.")
            return DistillSample(
                session_id=task.session_id,
                source_index=task.source_index,
                user_turn_index=task.user_turn_index,
                instruction=task.instruction,
                output=output,
            )
        except Exception as exc:
            last_error = exc
            if attempt >= max_retries:
                break
            sleep_seconds = min(max_backoff, initial_backoff * (2 ** (attempt - 1)))
            sleep_seconds += random.uniform(0.0, 0.25 * sleep_seconds)
            await asyncio.sleep(sleep_seconds)

    raise RuntimeError(f"Teacher generation failed after {max_retries} attempts: {last_error}")


def build_tasks(
    memory_index: dict[str, list[dict[str, Any]]],
    dataset: list[dict[str, Any]],
    *,
    context_turns: int,
    start_ratio: float | None,
    min_user_inputs: int,
    max_user_inputs: int,
    min_input_chars: int,
    max_input_chars: int,
    min_input_score: float,
    skip_missing_memory: bool,
) -> list[DistillTask]:
    tasks: list[DistillTask] = []

    for source_index, item in enumerate(dataset):
        session_id = session_id_for(source_index, item)
        memories = memory_index.get(session_id)
        if memories is None:
            # The injector may have used generated ids even when the raw dataset
            # has no id fields. Keep this fallback to preserve index alignment.
            memories = memory_index.get(default_session_id(source_index))

        if not memories and skip_missing_memory:
            continue

        user_inputs = user_inputs_after_context(
            item,
            context_turns=context_turns,
            start_ratio=start_ratio,
            max_user_inputs=max_user_inputs,
            min_input_chars=min_input_chars,
            max_input_chars=max_input_chars,
            min_input_score=min_input_score,
        )
        if len(user_inputs) < min_user_inputs:
            continue

        memory_background = format_memory_background(memories or [])
        for turn_index, user_text in user_inputs:
            tasks.append(
                DistillTask(
                    session_id=session_id,
                    source_index=source_index,
                    user_turn_index=turn_index,
                    instruction=user_text,
                    memory_background=memory_background,
                )
            )

    return tasks


async def run_generation(args: argparse.Namespace) -> list[DistillSample]:
    if not args.api_key:
        raise RuntimeError(
            "Missing teacher API key. Set TEACHER_API_KEY/OPENAI_API_KEY or pass --api-key. "
            "For local OpenAI-compatible servers, pass --api-key EMPTY."
        )

    memory_bank = load_json(args.memory_bank)
    raw_dataset = normalize_dataset(load_json(args.dataset))
    if args.max_sessions is not None:
        raw_dataset = raw_dataset[: args.max_sessions]

    start_ratio = args.start_ratio
    if start_ratio is None:
        metadata = memory_bank.get("metadata", {}) if isinstance(memory_bank, dict) else {}
        raw_ratio = metadata.get("turn_ratio")
        if isinstance(raw_ratio, (int, float)):
            start_ratio = float(raw_ratio)

    memory_index = build_memory_index(memory_bank)
    tasks = build_tasks(
        memory_index,
        raw_dataset,
        context_turns=args.context_turns,
        start_ratio=start_ratio,
        min_user_inputs=args.min_user_inputs,
        max_user_inputs=args.max_user_inputs,
        min_input_chars=args.min_input_chars,
        max_input_chars=args.max_input_chars,
        min_input_score=args.min_input_score,
        skip_missing_memory=args.skip_missing_memory,
    )

    if args.max_samples is not None:
        tasks = tasks[: args.max_samples]

    print(f"[INFO] memory_bank={args.memory_bank}")
    print(f"[INFO] dataset={args.dataset}")
    print(f"[INFO] teacher_model={args.model}")
    print(f"[INFO] api_base={args.api_base}")
    print(f"[INFO] source_sessions={len(raw_dataset)}")
    print(f"[INFO] memory_sessions={len(memory_index)}")
    if start_ratio is not None:
        print(f"[INFO] user_input_start=after {start_ratio:.2%} of each session")
    else:
        print(f"[INFO] user_input_start=after first {args.context_turns} non-system turns")
    max_inputs_label = "all" if args.max_user_inputs <= 0 else str(args.max_user_inputs)
    print(
        "[INFO] user_input_filter="
        f"min_chars={args.min_input_chars}, max_chars={args.max_input_chars}, "
        f"min_score={args.min_input_score}, max_user_inputs={max_inputs_label}"
    )
    print(f"[INFO] distill_tasks={len(tasks)}")
    print(f"[INFO] output={args.output}")

    client = AsyncOpenAI(api_key=args.api_key, base_url=args.api_base)
    semaphore = asyncio.Semaphore(args.concurrency)
    progress = tqdm(total=len(tasks), desc="Generating SFT data")
    samples: list[DistillSample] = []

    async def run_one(task: DistillTask) -> DistillSample | None:
        async with semaphore:
            try:
                return await generate_teacher_reply(
                    client,
                    task,
                    model=args.model,
                    max_retries=args.max_retries,
                    initial_backoff=args.initial_backoff,
                    max_backoff=args.max_backoff,
                )
            except Exception as exc:
                append_failed(
                    args.failed_output,
                    {
                        "session_id": task.session_id,
                        "source_index": task.source_index,
                        "user_turn_index": task.user_turn_index,
                        "instruction": task.instruction,
                        "error": repr(exc),
                    },
                )
                print(
                    f"[ERROR] session={task.session_id} turn={task.user_turn_index} failed: {exc}",
                    file=sys.stderr,
                )
                return None
            finally:
                progress.update(1)

    pending = [asyncio.create_task(run_one(task)) for task in tasks]
    for result in await asyncio.gather(*pending):
        if result is not None:
            samples.append(result)

    progress.close()
    return samples


